#include <bits/stdc++.h>
#include <cmath>
#include <algorithm>
#include <vector>
#include <string>

using namespace std;

// * Ques : Print the numbers in reverse order using loop

int main()
{

    int n;
    cout << "Enter n : ";
    cin >> n;

    for (int i = n; i >= 1; i--)
    {
        cout << i << endl;
    }

    return 0;
}