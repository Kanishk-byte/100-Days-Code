#include <iostream>
using namespace std;
int main()
{

    // Calculate the percentage of the following numbers

    float x1 = 90;
    float x2 = 91;
    float x3 = 92;
    float x4 = 93;
    float x5 = 96;

    float percent = (x1 + x2 + x3 + x4 + x5) / 5;
    cout << percent << "%" << endl;

    return 0;
}